# f(n) = 1 n <2
# f(n) = f(n/3) + 1  n >=2 n %3 ==0
# f(n) = f(n-2) + 5 n >=2 n %3 !=0
# найти кол во значений n на отрезке [1 100001] для которых f(n) == 55
f = [0] * 100001
for n in range(1, 100001):
    if n < 2:
        f[n] = 1
    elif n % 3 == 0:
        f[n] = f[n // 3] + 1
    else:
        f[n] = f[n-2] + 5
print(f.count(55))